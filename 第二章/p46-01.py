##关系运算符
#==等于：表示左右两个操作数是否相等，如果相等则整个表达式的值为True；不相等则为False
num1=15
num2=20
print(num1==num2)
#!=不等于
print(num1!=num2)
#>大于
print(num1>num2)
#<小于
print(num1<num2)
#>=大于等于：num1大于或者等于num2，条件都成立
print(num1>=num2)
#<=小于等于：num1小于或者等于num2，条件都成立
print(num1<=num2)
if num1>=num2:
    print("条件成立！")